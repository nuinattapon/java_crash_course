package me.nattapon.java.example;

public class SwitchExample {
    public static void main(String[] args) {
        String name = "nui";

        switch (name) {
            case "nui":
                System.out.println("Hi nui!");
                System.out.println("Hi nui!");
            break;
            default:
                System.out.println("Hi!");
        }
    }
}
